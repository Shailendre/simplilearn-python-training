# statistics
import statistics

ll = [1,2,3,4,5]

# mean
print ("mean:", statistics.mean(ll))
# meadian
print ("median:",statistics.median(ll))
# mode
print ("mode:",statistics.mode(ll))
# std deviation
print ("std. deviation:", statistics.stdev(ll))
# variance
print ("variance:",statistics.variance(ll))
